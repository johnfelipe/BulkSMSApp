        // POST: /Account/ForgotPassword
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> ForgotPassword(ForgotPasswordViewModel model)
        {
            if (ModelState.IsValid)
            {
                try
                {
                    var user = await UserManager.FindByNameAsync(model.Email);
                    if (user == null)
                    {
                        Danger("El Email ingresado no corresponde a un usuario registrado", true);
                        //return View();
                        // Don't reveal that the user does not exist or is not confirmed
                        //return View("ForgotPasswordConfirmation");
                    }
                    else
                    {
                        // For more information on how to enable account confirmation and password reset please visit http://go.microsoft.com/fwlink/?LinkID=320771
                        // Send an email with this link
                        string code = await UserManager.GeneratePasswordResetTokenAsync(user.Id);
                        var callbackUrl = Url.Action("ResetPassword", "Account", new { userId = user.Id, code = code }, protocol: Request.Url.Scheme);
                        await UserManager.SendEmailAsync(user.Id, "Reset Password", callbackUrl);
                        //return RedirectToAction("ForgotPasswordConfirmation", "Account");
                        Information("Por favor revisa tu Email y sigue las instrucciones para reestablecer tu Contraseña");
                        return RedirectToAction("Login");
                    }

                }
                catch (Exception ex)
                {
                    Danger("Ha ocurrido un error interno en la aplicación, por favor contacta al administrador del sistema \n" + ex.Message, true);
                    return View(model);
                }

            }
            else
            {
                Warning("Error de Validación, verifica los datos e intenta nuevamente", true);
            }
            return View(model);
        }

