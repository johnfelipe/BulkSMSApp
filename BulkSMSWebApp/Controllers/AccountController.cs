        //
        // POST: /Account/ResetPassword
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> ResetPassword(ResetPasswordViewModel model)
        {
            if (!ModelState.IsValid)
            {
                return View(model);
            }

            try
            {
                var user = await UserManager.FindByNameAsync(model.Email);

                if (user == null)
                {
                    // Don't reveal that the user does not exist
                    Danger("El email ingresado no es válido");
                    return View(model);
                }
                else
                {
                    var result = await UserManager.ResetPasswordAsync(user.Id, model.Code, model.Password);
                    if (model.Code == null)
                    {
                        Danger("El enlace de recuperación de contraseña ha expirado, solicita uno nuevo para reestablecer tu contraseña");
                        return RedirectToAction("ForgotPassword", "Account");
                    }
                    if (result.Succeeded)
                    {

                        Information("Tu Contraseña ha sido reestablecida correctamente, ya puedes iniciar sesión");
                        return RedirectToAction("Login", "Account");
                    }
                }

            }
            catch (Exception ex)
            {
                Danger("Ha ocurrido un error interno en la aplicación, por favor contacta al administrador del sistema \n" + ex.Message, true);
                return View(model);
            }
            
            return View();
        }

